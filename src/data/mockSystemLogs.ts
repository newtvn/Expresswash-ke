// ─── Helpers ──────────────────────────────────────────────────

/** ISO timestamp for `minutes` minutes ago. */
const mAgo = (minutes: number): string => {
  const d = new Date();
  d.setMinutes(d.getMinutes() - minutes);
  return d.toISOString();
};

const hAgo = (hours: number): string => mAgo(hours * 60);
const dAgo = (days: number, extraHours = 0): string =>
  mAgo(days * 1440 + extraHours * 60);

