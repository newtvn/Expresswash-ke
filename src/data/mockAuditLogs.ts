// ─── Helpers ──────────────────────────────────────────────────

/** ISO timestamp for `minutes` minutes ago, with optional extra hour offset. */
const minutesAgo = (minutes: number): string => {
  const d = new Date();
  d.setMinutes(d.getMinutes() - minutes);
  return d.toISOString();
};

const hoursAgo = (hours: number): string => minutesAgo(hours * 60);
const daysAgo = (days: number, extraHours = 0): string =>
  minutesAgo(days * 1440 + extraHours * 60);

